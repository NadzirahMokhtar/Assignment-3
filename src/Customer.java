/**
 * The type Customer.
 * encapsulation
 */
public class Customer {

    private String name;

    /**
     * Instantiates a new Customer.
     *
     * @param name the name
     */
    public Customer(String name) {
        this.name = name;
    }

    /**
     * Gets name.
     *
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * Sets name.
     *
     * @param name the name
     */
    public void setName(String name) {
        this.name = name;
    }
}



